import numpy as np

print(np.zeros(3))         # Create zeros array
print(np.zeros([3,3]),'\n')     # Create zero table array

print(np.ones(3))         # Create ones array
print(np.ones([3,3]),'\n')     # Create ones table array

print(np.eye(3),'\n')           #array with 0's and 1's

print(np.arange(4),'\n')        # Array from 4 elements 0-3

print(np.arange(4.),'\n')       # Array from 0-3 decimal